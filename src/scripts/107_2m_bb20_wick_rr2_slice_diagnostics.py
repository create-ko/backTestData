def run():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    trades, trading_days = load_trades()
    diagnostics = build_diagnostics(trades, trading_days)
    yearly = grouped_report(trades, ["year"], trading_days)
    monthly = grouped_report(trades, ["month"], trading_days)

    trades.to_csv(OUTPUT_DIR / "rr2_slice_enriched_trades.csv", index=False, encoding="utf-8-sig")
    diagnostics.to_csv(OUTPUT_DIR / "rr2_slice_diagnostics.csv", index=False, encoding="utf-8-sig")
    yearly.to_csv(OUTPUT_DIR / "rr2_slice_yearly_baseline.csv", index=False, encoding="utf-8-sig")
    monthly.to_csv(OUTPUT_DIR / "rr2_slice_monthly_baseline.csv", index=False, encoding="utf-8-sig")
    write_html(diagnostics, yearly, monthly)

    print("=== 2M BB20 WICK BB4 RR2 SLICE DIAGNOSTICS ===")
    print("Trading days:", trading_days)
    print(diagnostics.head(40).to_string(index=False))
    print("WROTE:", OUTPUT_DIR)


if __name__ == "__main__":
    run()
